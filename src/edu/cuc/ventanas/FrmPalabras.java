    private void txtPalabraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPalabraKeyTyped

        char c = evt.getKeyChar();

        if (!Character.isLetter(c)) {
            getToolkit();
            evt.consume();
        }
    }//GEN-LAST:event_txtPalabraKeyTyped

