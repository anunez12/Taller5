    private void introducirPalabra() throws HeadlessException {
        palabraNueva = new Palabra(txtPalabra.getText());
        //Caja
        Palabra palabraNueva = new Palabra(txtPalabra.getText());
        txtPalabra.setText(null);
        JOptionPane.showMessageDialog(this,
                "Palabra ingresada en el sistema!",
                "Confirmación",
                JOptionPane.INFORMATION_MESSAGE);
    }

    private void introducirSinonimo() throws HeadlessException {

        palabraNueva.adicionarSinonimo(new Palabra(txtSinonimo.getText()));
        txtSinonimo.setText(null);
        JOptionPane.showMessageDialog(this,
                "Sinonimo ingresado en el sistema!",
                "Confirmación",
                JOptionPane.INFORMATION_MESSAGE);           
    }

