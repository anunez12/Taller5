    private void cmdIngresarPalabraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdIngresarPalabraActionPerformed

        if (txtPalabra.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese palabra", "Advertencia", JOptionPane.WARNING_MESSAGE);
            txtPalabra.requestFocusInWindow();
        } else {
            introducirPalabra();

            JButton botonesHabilitar[] = {cmdIngresarPalabra1};
            JButton botonesDesabilitar[] = {cmdIngresarPalabra, cmdCantidad, cmdPromedio, cmdMayor};

            edu.cuc.palabras.Palabra.habilitarBotones(botonesHabilitar);
            edu.cuc.palabras.Palabra.deshabilitarBotones(botonesDesabilitar);
        }

    }//GEN-LAST:event_cmdIngresarPalabraActionPerformed

    private void cmdIngresarPalabra1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdIngresarPalabra1ActionPerformed

        if (txtSinonimo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Adiccione sinonimo", "Advertencia", JOptionPane.WARNING_MESSAGE);
            txtSinonimo.requestFocusInWindow();
        } else {
            introducirSinonimo();

            JButton botonesHabilitar[] = {cmdCantidad, cmdPromedio, cmdMayor};
            JButton botonesDesabilitar[] = {cmdIngresarPalabra, cmdIngresarPalabra1};

            edu.cuc.palabras.Palabra.habilitarBotones(botonesHabilitar);
            edu.cuc.palabras.Palabra.deshabilitarBotones(botonesDesabilitar);  
        }
    }//GEN-LAST:event_cmdIngresarPalabra1ActionPerformed

